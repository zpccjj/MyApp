package bean;

public class MediumStatistics {
	private String MediumCode;//介质代码
	private String MediumName;
	private int MediumNameCount;
	public String getMediumCode() {
		return MediumCode;
	}
	public void setMediumCode(String mediumCode) {
		MediumCode = mediumCode;
	}
	public String getMediumName() {
		return MediumName;
	}
	public void setMediumName(String mediumName) {
		MediumName = mediumName;
	}
	public int getMediumNameCount() {
		return MediumNameCount;
	}
	public void setMediumNameCount(int mediumNameCount) {
		MediumNameCount = mediumNameCount;
	}


}
